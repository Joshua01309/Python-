# Operadores Aritméticos en Python

# Suma
a = 10
b = 5
suma = a + b
print(f'Suma: {a} + {b} = {suma}')  # Suma: 10 + 5 = 15

# Resta
resta = a - b
print(f'Resta: {a} - {b} = {resta}')  # Resta: 10 - 5 = 5

# Multiplicación
multiplicacion = a * b
print(f'Multiplicación: {a} * {b} = {multiplicacion}')  # Multiplicación: 10 * 5 = 50

# División Regresa un valor flotante
division = a / b
print(f'División: {a} / {b} = {division}')  # División: 10 / 5 = 2.0
# División Entera
division_entera = a // b
print(f'División Entera: {a} // {b} = {division_entera}')  # División Entera: 10 // 5 = 2
# División baja Devuelve entero redondeado hacia abajo

# Módulo
modulo = a % b
print(f'Módulo: {a} % {b} = {modulo}')  # Módulo: 10 % 5 = 0

# Exponenciación
exponenciacion = a ** b
print(f'Exponenciación: {a} ** {b} = {exponenciacion}')  # Exponenciación: 10 ** 5 = 100000

# Tipo de dato: nos devuelve que tipo de dato es
type_data = type('hola')
print(type_data)
