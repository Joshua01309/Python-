
# AND con operadores bit a bit
resultado_bit_and_1 = True & True
resultado_bit_and_2 = False & True
resultado_bit_and_3 = True & False
resultado_bit_and_4 = False & False
print(f'True & True = {resultado_bit_and_1}')
print(f'False & True = {resultado_bit_and_2}')
print(f'True & False = {resultado_bit_and_3}')
print(f'False & False = {resultado_bit_and_4}')

# OR con operadores bit a bit
resultado_bit_or_1 = True | True
resultado_bit_or_2 = False | True
resultado_bit_or_3 = True | False
resultado_bit_or_4 = False | False
print(f'True | True = {resultado_bit_or_1}')
print(f'False | True = {resultado_bit_or_2}')
print(f'True | False = {resultado_bit_or_3}')
print(f'False | False = {resultado_bit_or_4}')

# NOT con operadores bit a bit
resultado_bit_not_1 = not True
resultado_bit_not_2 = not False
print(f'NOT True = {resultado_bit_not_1}')
print(f'NOT False = {resultado_bit_not_2}')